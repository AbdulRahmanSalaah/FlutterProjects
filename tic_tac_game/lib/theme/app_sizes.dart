import 'package:flutter/material.dart';

// gap package is used to create gaps between widgets in the app UI design
import 'package:gap/gap.dart';


// gapXs() creates a gap of 2 pixels
Gap gapXs() => const Gap(2);

Gap gapS() => const Gap(4);

Gap gapM() => const Gap(8);

Gap gapL() => const Gap(16);

Gap gapXL() => const Gap(24);

Gap gap2XL() => const Gap(32);

Gap gap3XL() => const Gap(48);

Gap gap4XL() => const Gap(64);

borderRadiusXs() => BorderRadius.circular(4);

borderRadiusS() => BorderRadius.circular(8);

borderRadiusM() => BorderRadius.circular(12);
